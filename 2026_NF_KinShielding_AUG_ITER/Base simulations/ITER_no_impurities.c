double interp2d(int n_x, int n_y, const double x[], const double y[], const double z[][n_y], double xp, double yp) {

  int i_lo;
  int j_lo;
   
  //printf("ran interp2d"); 

  if(xp < x[0]) {
    i_lo = 0;
  }
  else if(xp >= x[n_x-1]) {
      i_lo = n_x-2;
  }
  else {
    for (int i=0; i<n_x-1; i++) {
        if (x[i] <= xp && xp < x[i+1]) {
            i_lo = i;
            break;
        }
    }
  }
  
  if (yp < y[0]) {
    j_lo = 0;
  }
  else if (yp >= y[n_y-1]) {
    j_lo = n_y-2;
  }
  else {
    for (int j=0; j<n_y-1; j++) {
      if (y[j] <= yp && yp < y[j+1]) {
        j_lo = j;
        break;
      }
    }
  }
  
  double x_frac = (xp-x[i_lo])/(x[i_lo+1]-x[i_lo]);
  double y_frac = (yp-y[j_lo])/(y[j_lo+1]-y[j_lo]);

  double gradx = ((1-y_frac)*(z[i_lo+1][j_lo]-z[i_lo][j_lo])+y_frac*(z[i_lo+1][j_lo+1]-z[i_lo][j_lo+1]))/(x[i_lo+1]-x[i_lo]);
  double grady = ((1-x_frac)*(z[i_lo][j_lo+1]-z[i_lo][j_lo])+x_frac*(z[i_lo+1][j_lo+1]-z[i_lo+1][j_lo]))/(y[j_lo+1]-y[j_lo]);

  return z[i_lo][j_lo] + gradx*(xp-x[i_lo])+grady*(yp-y[j_lo]);
}

