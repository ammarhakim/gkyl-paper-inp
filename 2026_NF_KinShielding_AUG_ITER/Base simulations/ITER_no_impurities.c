void divertor_plate_func_out(double s, double* RZ)
{
  const int npts = 12;
  double t[] = {
    0.    , 0.0909, 0.1818, 0.2727, 0.3636, 0.4545,
    0.5454, 0.6363, 0.7272, 0.8181, 0.9090, 1. 
  };
  double R[] = {
    5.5543, 5.5543, 5.5543, 5.5543, 5.5543, 5.5543,
    5.5543, 5.5543, 5.5543, 5.5542, 5.5572, 5.5630
  };
  double Z[] = {
    -4.3885, -4.3775, -4.3621, -4.3424, -4.3183, -4.2895,
    -4.2558, -4.2164, -4.1701, -4.1146, -4.0580, -3.9939
  };
  // Find indices in t that bound s.
  int idx_tlo, idx_tup;
  if (s < 1e-8) {
    idx_tlo = 0;
    idx_tup = 0;
  }
  else if (fabs(s-1.0) < 1e-8) {
    idx_tlo = npts-1;
    idx_tup = npts-1;
  }
  else {
    for (int i=0; i<npts-1; i++) {
      if (t[i] <= s && s < t[i+1]) {
        idx_tlo = i;
        idx_tup = i+1;
        break;
      }
    }
  }
  // Interpolate the value of R and Z.
  double Dt = t[idx_tup]-t[idx_tlo];
  if (idx_tlo == idx_tup) {
    RZ[0] = R[idx_tlo];
    RZ[1] = Z[idx_tlo];
  }
  else {
    RZ[0] = ((s-t[idx_tlo])/Dt)*R[idx_tup] + ((t[idx_tup]-s)/Dt)*R[idx_tlo];
    RZ[1] = ((s-t[idx_tlo])/Dt)*Z[idx_tup] + ((t[idx_tup]-s)/Dt)*Z[idx_tlo];
  }
}

